#include<stdio.h>
/**
 * add- add of two int
 * @a:first int
 * @b:second int
 * Return:additiona
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - add of two int
 * @a:first int
 * @b:second int
 * Return:subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * mult-multiply num
 * @a:first int
 * @b:second int
 * Return:additiona
 */

int mult(int a, int b)
{
	return (a * b);
}
/**
 * mod- add of two int
 * @a:first int
 * @b:second int
 * Return:modulas
 */

int mod(int a, int b)
{
	return (a % b);
}
/**
 * div- div of two int
 * @a:first int
 * @b:second int
 * Return:divison
 */

int div(int a, int b)
{
	return (a / b);
}
