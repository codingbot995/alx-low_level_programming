#include<stdio.h>
/**
 * add - add two nums
 * @a:num 1
 * @b:nums 2
 * Return :addition
 */

int  sum(int a,  int b)
{
	return (a + b);
}
/**
 * sub - sum two nums
 * @a:num 1
 * @b:nums 2
 * Return :addition
 */

int  sub(int a,  int b)
{
	return (a - b);
}
/**
 * mod - mod two nums
 * @a:num 1
 * @b:nums 2
 * Return :addition
 */

int  mod(int a,  int b)
{
	return (a % b);
}
/**
 * div:divide two nums
 * @a:num 1
 * @b:nums 2
 * Return :addition
 */

int  div(int a,  int b)
{
	return (a / b);
}
/**
 * mul - add two nums
 * @a:num 1
 * @b:nums 2
 * Return :addition
 */

int  mul(int a,  int b)
{
	return (a * b);
}
